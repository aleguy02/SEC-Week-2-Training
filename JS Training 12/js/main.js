// Loops
let myNumber = 50;
while (myNumber < 50) {
  myNumber += 2;
  console.log(myNumber);
}


// do while loop executes AT LEAST once
do {
  console.log(myNumber);
} while (myNumber < 50)

for (let i = 0; i <= 10; i++) {
  console.log(i);
}

